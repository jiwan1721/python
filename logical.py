#and - True if both the operands are true
#or - True if either of the operands is true
#not - True if operand is false
x = True
y = False
print("usin and operator: ",x and y)
print("using or operator: ", x or y)
print("using not operator: ",not x)
print("using not operator: ",not y)


#Bitwise operator

# & - Bitwise AND
x =12
y =23
print("bitwise And",x&y)

# | Bitwise or
print("bitwise or",x or y)

# ~ Bitwise Not
print("bitwise not: ",x|y)

# ^ Bitwise XOR
print("bitwise XOR: ",x^y)

# >> Bitwise right shift
print("Bitwise right: ",x>>2)

# << Bitwise left shift
print("Bitwise left shift: ",x<<4)
